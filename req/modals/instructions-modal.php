				<h5>Window Size</h5>
				<hr/>
				<p>It is important that you use the CAS Planner full screen on your computer. If you do not, then there is a possibility that the various components may not display correctly.</p>
				<hr/>
				<h5>Adding chits</h5>
				<hr/>
				<p>
					<strong>Step 1:</strong> Click the map or enter MGRS grids and click "Fly to Coordinates."
					<br/>
					<strong>Step 2:</strong> Enter a description (optional).
					<br/>
					<strong>Step 3:</strong> Click on a chit to add to the map.
				</p>
				<p>
					If adding a threat ring or CAP, a separate window will open to gather further details. <strong>NOTE:</strong> The point you click is the center of the CAP.
					<br/>
					Clicking on a chit will allow you to rename or delete it.
					<br/>
					You can add a 9-Line to a threat/hostile chit and a 15-Line to a SRV chit.
				</p>
				<hr/>
				<h5>Analysis Tool</h5>
				<hr/>
				<p>
					Click on the <img src="./images/ruler-icon.png"> icon then click the map to start the ruler and click again to add a vertex. Additional clicks will continue the ruler.
					<br/>
					Press <code>ESC</code> to end the current ruler, you can then start a new ruler. Pressing <code>ESC</code> again to exit the tool.
				</p>
				<hr/>
				<h5>Drawing Tools</h5>
				<hr/>
				<p>
					Click on any of the shapes to add to the map. Clicking will add a vertex to lines and polygons. Clicking the last point will end the line or shape. You can also click the actions on the left to delete the last point or finish the line. Pressing <code>ESC</code> will also cancel the line.
					<br/>
					For Engagement Areas or ROZs, click and drag on the map to create the shape.
					<br/>
					Whatever is in the <code>Description</code> box will be used as the name of the shape.
					<br/>
					Clicking on a shape will allow you to rename or delete it.
				</p>
				<hr/>
				<h5>Layers</h5>
				<hr/>
				<p>
					You can choose between two imagery types using the layers menu <img src="./images/layers.png">.
					<br/>
					You can also toggle map labels, airspace overlays, and MGRS grids from the layers menu.
				</p>
			</div>
